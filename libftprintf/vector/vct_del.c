#include "vector.h"

/* ***************************************************************************
	Free the vector and set it to NULL
**************************************************************************** */

void			vct_del(t_vector **vector)
{
	if (vector && *vector)
	{
		free((*vector)->str);
		(*vector)->str = NULL;
		free(vector);
		vector = NULL;
	}
}
